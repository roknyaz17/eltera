# Категории, которые НЕ относятся к профнаправлениям (служебные) — бэкфилл их не трогает.
SERVICE_CATEGORIES = {"360", "адаптация", "performance", "performance review"}


def match_direction(text: str | None) -> str | None:
    """Подбирает slug направления по тексту категории/названия (или None)."""
    t = (text or "").strip().lower()
    if not t or t in SERVICE_CATEGORIES:
        return None
    # сначала точное совпадение с названием направления
    for slug, title in DIRECTIONS:
        if t == title.lower():
            return slug
    # затем по алиасам-подстрокам
    for slug, aliases in DIRECTION_ALIASES.items():
        if any(a in t for a in aliases):
            return slug
    return None
